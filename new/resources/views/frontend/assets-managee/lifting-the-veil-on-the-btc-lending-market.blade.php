                            <p><span style="font-weight: 400;">The futures market is a great indicator for the health of the crypto lending market. As we’ve outlined previously, BlockFi lends crypto out to institutions in order to generate a yield for BIA customers. We’ve found that the BTC lending market is by far the most active and most liquid.</span>
                            </p>
                            <p><span style="font-weight: 400;">With that in mind, we wanted to continue providing insights for our community about the types of companies participating in the crypto lending market. These roughly breakdown as 70% Market Makers, 25% Funds, and 5% Exchanges.</span>
                            </p>
                            <p><span style="font-weight: 400;">Some of the products and companies enabled by borrowing crypto are as follows:</span>
                            </p>
                            <ul>
                                <li style="font-weight: 400;"><span style="font-weight: 400;">Spot &amp; Derivatives (Futures, Perpetuals, Options)</span>
                                </li>
                                <li style="font-weight: 400;"><span style="font-weight: 400;">Derivatives (OKEX, BitMex, CME)</span>
                                </li>
                                <li style="font-weight: 400;"><span style="font-weight: 400;">Quanto Swaps (BitMex, OKEX)</span>
                                </li>
                                <li style="font-weight: 400;"><span style="font-weight: 400;">Arbitrage</span></li>
                                <li style="font-weight: 400;"><span
                                            style="font-weight: 400;">Margin Lending (Exchanges)</span></li>
                            </ul>
                            <p><span style="font-weight: 400;">When we lay this out for our customers we often get asked why a company like an exchange needs to borrow crypto. The main reason is that it allows exchanges to maximize its ability to provide a liquid marketplace, making it easier to execute trades quickly and efficiently. When you look at the CBOE (currently the second largest U.S. equities market operator in the world), they facilitate an average volume of 1,001,716,592 trades per day over the four exchanges they operate. In order to ensure smooth transactions for both parties of a trade, exchanges need to have enough liquid inventory on-hand. Lending companies like BlockFi bolster markets by creating additional liquidity. </span>
                            </p>
